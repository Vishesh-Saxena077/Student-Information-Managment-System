    <span style="color:white"><h2>History</h2></span><hr>
<span style=" font-size: 16;
    color: deeppink;"><p class="ex1">
Main building, Graphic Era University
    Graphic Era was founded in 1993 and started with undergraduate programs in core engineering and allied sciences. In 2008 the institute was accorded the status of deemed university.In 2015 Graphic Era was accredited by the National Assessment and Accreditation Council (NAAC) with grade ‘A’.</p><hr>
    
    <span style="color:white"><h2>Recognition and accreditation</h2></span><hr>
    <p class="ex1">
   - Deemed university status under General Category August 14, 2008 See: Ministry of Human Resource Development, GOI, Notification F, 9-48/2007 U-3(A) Under Section 3 of the UGC Act 1956.[1]
-Accredited by National Assessment Accreditation Council (NAAC) with grade 'A'.
-As of the 6 June 2018, it is the only private university in the state of Uttarakhand to have obtained an approval from AICTE (All India Council for Technical Education).[4]
        -In 2019 the National Institutional Ranking Framework ranked Graphic Era the second highest engineering institution in Uttarakhand, after IIT Roorkee.</p>
